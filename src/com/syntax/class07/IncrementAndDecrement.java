package com.syntax.class07;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
		int num=10;
		
		//all the same below (++ increments adds by 1)
		num=num+1;
		
		num+=1;
		
		num++;
		
		System.out.println(num);
		
		// -- decrement by 1
		
		num--;
		num--;
		num--;
		
		System.out.println(num);
		
		

	}

}
